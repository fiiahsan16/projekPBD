    private void tcariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tcariKeyReleased
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "select * from tbl_lapor where kd_lapor like '%" + tcari.getText() + "%' or pemasukan like'%" + tcari.getText() + "%' or ket_masuk like'" + tcari.getText() + "%' or pengeluaran like'%" + tcari.getText() + "%' " + "or ket_keluar like'%" + tcari.getText() + "%'";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[5];
                o[0] = r.getString("kd_lapor");
                o[1] = r.getString("pemasukan");
                o[2] = r.getString("ket_masuk");
                o[3] = r.getString("pengeluaran");
                o[4] = r.getString("ket_keluar");
                
                
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }
    }//GEN-LAST:event_tcariKeyReleased

