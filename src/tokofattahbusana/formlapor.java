    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        if(tkdlapor.getText().equals("") ||tpemasukan.getText().equals("") || tketpemas.getText().equals("")|| tpengeluaran.getText().equals("")|| tketpenge.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Toko baju fattah busana", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String kdlapor = tkdlapor.getText();
            String pmasuk = tpemasukan.getText();
            String kmasuk = tketpemas.getText();
            String pkeluar = tpengeluaran.getText();
            String kkeluar = tketpenge.getText();
           
            try {
                 
                
                Connection c = koneksi.getKoneksi();

                String sql = "INSERT INTO tbl_lapor VALUES (?, ?, ?, ?, ?)";

                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, kdlapor);
                p.setString(2, pmasuk);
                p.setString(3, kmasuk);
                p.setString(4, pkeluar);
                p.setString(5, kkeluar);
                
                
                p.executeUpdate();
                p.close();
                
            } catch (SQLException e) {
                System.out.println("Terjadi Error"+e);
            } finally {
                loadData();
                kode();
                
                tpemasukan.setText("");
                tketpemas.setText("");
                tpengeluaran.setText("");
                tketpenge.setText("");

                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "Toko baju fattah busana", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_bsimpanActionPerformed

